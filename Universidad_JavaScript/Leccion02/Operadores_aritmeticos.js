let a = 3, b = 2;
let z = a + b;
console.log(z);

z = a - b;
console.log(z);

z = a * b;
console.log(z);

z = a / b;
console.log(z);

z = a % b;
console.log(z);

z = a ** b;
console.log(z);

// Inccremento
// Pre-incremento 
z = ++a;
console.log(a);
console.log(z);

// Post-incremento

z = b++;
console.log(b);
console.log(z);

// Decremento
// Pre-dremento
z = --a;
console.log(a);
console.log(z);

// Post-decremento
z = a--;
console.log(a);
console.log(z);